var color = 'red';
switch (color) {
  case "blue":
    console.log("Blue");
    break;
  case "green":
    console.log("Green");
    break;
  case "orange":
    console.log("Orange");
    break;
  case "indigo":
    console.log("Indigo");
    break;
  case "purple":
    console.log("Purple");
    break;
  case "red":
    console.log("Red");
    break;
  default:
    console.log("black");
}